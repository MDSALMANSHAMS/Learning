// maths operators
const age = 2024 - 1999;
console.log(age);
console.log(age * 2, age / 5);

const first_set = "Ice";
const last_set = "Cream";
console.log(first_set + last_set);

// assignment operators
let x = 10 + 5;
x = x + 5;
x -= 2;
x++;
console.log(x);

// comparison operators
y = 20;
console.log(x > y);
console.log(y >= x);
console.log(y - 5 >= x + 5);

// Q.

const avg_1 = x + y / 2;
const avg_2 = (x + y) / 2; // adding parenthesis (brackets, dashes, or commas)
console.log(avg_1);
console.log(avg_2);

